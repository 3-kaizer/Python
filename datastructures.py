# Data Structures
#  1. Lists
fruits=['Apples','Banana','Orange']
print(fruits)
print(fruits[2])
fruits[1]='watermelon'
print(fruits)

#  2. Sets
cars={'BMW','Mercedes','Audi'}
print(cars)
#  3. Tuples
colors=('Red','Green','Blue')
print(colors)
print(colors[2])

#  4. Dictionary
student={
    'name':'Dominic',
    'age':17,
    'course':'MIT'
}
print(student)
print(student['name'])
student['course']='Cyber Security'
print(student)